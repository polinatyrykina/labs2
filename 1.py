
print("Вычисление дискриминанта квадратного уравнения ax² + bx + c = 0")
print("=" * 50)

# Ввод коэффициентов
a = float(input("Введите коэффициент a: "))
b = float(input("Введите коэффициент b: "))
c = float(input("Введите коэффициент c: "))

# Вычисление дискриминанта
D = b**2 - 4*a*c

# Вывод результата
print(f"\nДискриминант D = {b}² - 4×{a}×{c} = {D}")

# Анализ дискриминанта
if D > 0:
    print("Дискриминант положительный. Уравнение имеет два различных корня.")
elif D == 0:
    print("Дискриминант равен нулю. Уравнение имеет один корень.")
else:
    print("Дискриминант отрицательный. Уравнение не имеет действительных корней.")